 public void readjTable(){
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
       
        historicoDAO dao = new historicoDAO();
       
        for(historico h: dao.read()){
           
            modelo.addRow(new Object[]{
                h.getCodigoProduto(),
                h.getSaldo()
            });
           
        }
    }


