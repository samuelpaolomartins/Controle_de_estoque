    public void readjTable(){
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        
        entradaDAO dao = new entradaDAO();
        
        for(entrada e: dao.read()){
            
            modelo.addRow(new Object[]{
                e.getCodigoProduto()
            });
            
        }
    }
